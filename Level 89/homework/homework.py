#  1. Initialization
# Initialization არის ობიექტის შექმნისას მონაცემების საწყისი მნიშვნელობებით შევსება. Python-ში ამისთვის გამოიყენება სპეციალური მეთოდი __init__.

#  2. Class Variables
# კლასის ცვლადებია ისეთი ცვლადები, რომლებიც გაზიარებულია ყველა ობიექტს შორის და გამოიყენება თავად კლასზე, არა კონკრეტულ ობიექტზე.

#  3. Instance Methods
# ეს არის მეთოდები, რომლებიც ეკუთვნის კონკრეტულ ობიექტს და მუშაობს მისი მონაცემებით. მათ ყოველთვის აქვთ self პარამეტრი.

#  4. Inheritance
# მემკვიდრეობა არის შესაძლებლობა, რომ ერთმა კლასმა გადმოიბაროს სხვა კლასის ატრიბუტები და მეთოდები.

#  5. Multiple Inheritance
# ეს არის ის შემთხვევა, როცა ერთი კლასი მემკვიდრეობს ერთზე მეტი კლასისგან.

#  6. Multilevel Inheritance
# მემკვიდრეობის ჯაჭვი, სადაც ერთი კლასი მემკვიდრეობს მეორე კლასისგან, ხოლო ეს მეორე – მესამე კლასისგან და ასე შემდეგ.

#  7. super()
# super() ფუნქცია გამოიყენება მშობელი კლასის მეთოდებისა და კონსტრუქტორების გამოსაძახებლად შვილ კლასში.

#  8. Abstract Classes
# აბსტრაქტული კლასები არის ისეთი კლასები, რომლებიც არ შეიძლება პირდაპირი ობიექტად შეიქმნას. გამოიყენება, როგორც შაბლონი შვილ კლასებისთვის. მათ ხშირად აქვთ აბსტრაქტული მეთოდები, რომლებიც აუცილებლად უნდა განისაზღვროს შვილ კლასებში.

#  9. Polymorphism
# პოლიმორფიზმი ნიშნავს, რომ ერთსა და იმავე მეთოდს შეიძლება ჰქონდეს განსხვავებული ქცევა სხვადასხვა კლასში.

#  10. Duck Typing
# Duck Typing არის კონცეფცია, რომლის მიხედვითაც ობიექტის ტიპი არ განსაზღვრავს მის ფუნქციურობას, მთავარია ქცევა — "თუ მუშაობს როგორც ჩიტი, ჩიტია".

#  11. Aggregation
# აგრეგაცია არის კავშირი ორ კლასს შორის, სადაც ერთი კლასი შეიცავს მეორეს, მაგრამ ეს ობიექტები ერთმანეთზე არ არიან მკაცრად დამოკიდებული.

#  12. Composition
# კომპოზიცია არის ძლიერი კავშირი ორ კლასს შორის, სადაც ერთი ობიექტი მკაცრად შედგება მეორედან და მის გარეშე ვერ იარსებებს.

#  13. Static Methods
# სტატიკური მეთოდები არის მეთოდები, რომლებიც არ იყენებს self-ს ან cls-ს და მუშაობს როგორც ჩვეულებრივი ფუნქცია კლასში.

#  14. Class Methods
# კლასური მეთოდები იყენებენ cls-ს და მოქმედებენ თავად კლასზე, არა კონკრეტულ ობიექტზე.

#  15. Data Hiding
# მონაცემების დამალვა არის ინფორმაციის დაცვის მექანიზმი, რომლის საშუალებითაც ცვლადები ან მეთოდები ვერ მოექცევიან პირდაპირ წვდომაში კლასის გარედან.